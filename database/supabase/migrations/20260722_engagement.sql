  p_monday_reward bigint,
  p_tuesday_reward bigint,
  p_wednesday_reward bigint,
  p_thursday_reward bigint,
  p_friday_reward bigint,
  p_saturday_reward bigint,
  p_sunday_reward bigint,
  p_makeup_cost bigint
