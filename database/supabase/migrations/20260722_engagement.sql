      p_wednesday_reward,
      p_thursday_reward,
      p_friday_reward,
      p_saturday_reward,
      p_sunday_reward,
      p_makeup_cost,
      v_admin_id
    )
    returning rule.* into v_rule;
  exception
    when unique_violation then
      raise exception 'CHECKIN_RULE_DATE_EXISTS';
