create or replace function public.validate_public_url(
  p_value text,
  p_allow_relative boolean default false
)
returns text
language plpgsql
immutable
set search_path = public, pg_temp
as $$
declare
  v_value text := btrim(p_value);
  v_authority text;
begin
  if p_value is null or v_value = '' then
    return null;
  end if;

  if v_value ~ '[[:cntrl:]]'
     or v_value ~ '[[:space:]]'
     or strpos(v_value, chr(92)) > 0 then
    raise exception 'INVALID_PUBLIC_URL';
  end if;

  if lower(left(v_value, 8)) = 'https://' then
    v_authority := split_part(
      split_part(
        split_part(substring(v_value from 9), '/', 1),
        '?',
        1
      ),
      '#',
      1
    );

    if v_authority = '' then
      raise exception 'INVALID_PUBLIC_URL';
    end if;

    return v_value;
  end if;

  if coalesce(p_allow_relative, false)
     and left(v_value, 1) = '/'
     and left(v_value, 2) <> '//' then
    return v_value;
  end if;

  raise exception 'INVALID_PUBLIC_URL';
end;
$$;

create or replace function public.list_active_activities()
returns table (
  id uuid,
  title text,
  body text,
  cover_url text,
  action_label text,
  action_url text,
  publish_at timestamptz,
  starts_at timestamptz,
  ends_at timestamptz,
  reward_amount bigint,
  claimed boolean,
  claimed_at timestamptz
)
language sql
stable
security definer
set search_path = public, pg_temp
as $$
  select
    activity.id,
    activity.title::text,
    activity.body,
    activity.cover_url,
    activity.action_label::text,
    activity.action_url,
